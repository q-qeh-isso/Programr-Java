package variableMaxValue;

/**
 * What are the minimum and maximum values integers can take in JAVA? 
 * The key here is to know what the values are, and how to get them 
 * when you need them.
 * 
 * Print them line by line:
 * Integer min
 * Integer max
 * 
 *  
 *
 */

public class VariableMaxValues {
	
	public static void main(String[] args) {

		int max = Integer.MAX_VALUE;
		int min = Integer.MIN_VALUE;
		
		System.out.println(min);
		System.out.println(max);
		
		
		
	}

}
